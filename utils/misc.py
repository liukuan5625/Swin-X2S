import numpy as np
import scipy.ndimage as ndimage
import torch
import open3d
from monai.transforms import Affine


def resample_3d(img, target_size):
    imx, imy, imz = img.shape
    tx, ty, tz = target_size
    zoom_ratio = (float(tx) / float(imx), float(ty) / float(imy), float(tz) / float(imz))
    img_resampled = ndimage.zoom(img, zoom_ratio, order=0, prefilter=False)
    return img_resampled


class AverageMeter(object):
    def __init__(self):
        self.reset()

    def reset(self):
        self.val = 0
        self.avg = 0
        self.sum = 0
        self.count = 0

    def update(self, val, n=1):
        self.val = val
        self.sum += val * n
        self.count += n
        self.avg = np.where(self.count > 0, self.sum / self.count, self.sum)


def distributed_all_gather(
    tensor_list, valid_batch_size=None, out_numpy=False, world_size=None, no_barrier=False, is_valid=None
):
    if world_size is None:
        world_size = torch.distributed.get_world_size()
    if valid_batch_size is not None:
        valid_batch_size = min(valid_batch_size, world_size)
    elif is_valid is not None:
        is_valid = torch.tensor(bool(is_valid), dtype=torch.bool, device=tensor_list[0].device)
    if not no_barrier:
        torch.distributed.barrier()
    tensor_list_out = []
    with torch.no_grad():
        if is_valid is not None:
            is_valid_list = [torch.zeros_like(is_valid) for _ in range(world_size)]
            torch.distributed.all_gather(is_valid_list, is_valid)
            is_valid = [x.item() for x in is_valid_list]
        for tensor in tensor_list:
            gather_list = [torch.zeros_like(tensor) for _ in range(world_size)]
            torch.distributed.all_gather(gather_list, tensor)
            if valid_batch_size is not None:
                gather_list = gather_list[:valid_batch_size]
            elif is_valid is not None:
                gather_list = [g for g, v in zip(gather_list, is_valid_list) if v]
            if out_numpy:
                gather_list = [t.cpu().numpy() for t in gather_list]
            tensor_list_out.append(gather_list)
    return tensor_list_out


def cal_localization_error_and_identity_rate(pred, label, scale):
    l_ls = np.unique(label)

    # if label just has background
    if len(l_ls) == 1:
        return 0, 1

    id_num = 0
    loc_ls = []
    for i in l_ls[1:]:
        l_idx = np.mean(np.argwhere(label == i)[:, -3:], axis=0)
        p_idx = np.mean(np.argwhere(pred == i)[:, -3:], axis=0)
        # if pred has no this class
        if not np.isnan(np.sum(np.mean(np.argwhere(pred == i)[:, -3:], axis=0))):
            dist = np.linalg.norm(l_idx - p_idx) * scale
            if dist < 20:
                id_num = id_num + 1
            loc_ls.append(dist)

    loc_error = np.mean(loc_ls)
    id_rate = id_num / (len(l_ls) - 1)
    return loc_error, id_rate
